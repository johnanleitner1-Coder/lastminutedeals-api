def _auto_check_003(source: str, tree: ast.AST, filepath: pathlib.Path) -> list[dict]:
    """
    Detects references to undefined names inside function bodies where a similarly-named
    parameter exists — catches typos like `hour` vs `hours_ahead`.
    """
    import ast

    issues = []

    for node in ast.walk(tree):
        if not isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef)):
            continue

        # Collect all names defined in this function's scope:
        # parameters, local assignments, for-loop targets, with-targets, comprehension vars
        defined = set()

        # Parameters (args, vararg, kwonlyargs, kwarg, posonlyargs)
        for arg in node.args.args + node.args.posonlyargs + node.args.kwonlyargs:
            defined.add(arg.arg)
        if node.args.vararg:
            defined.add(node.args.vararg.arg)
        if node.args.kwarg:
            defined.add(node.args.kwarg.arg)

        # Walk the function body for local definitions
        for child in ast.walk(ast.Module(body=node.body, type_ignores=[])):
            if isinstance(child, ast.Assign):
                for target in child.targets:
                    if isinstance(target, ast.Name):
                        defined.add(target.id)
                    elif isinstance(target, ast.Tuple):
                        for elt in ast.walk(target):
                            if isinstance(elt, ast.Name):
                                defined.add(elt.id)
            elif isinstance(child, (ast.AnnAssign, ast.NamedExpr)):
                if isinstance(getattr(child, 'target', None), ast.Name):
                    defined.add(child.target.id)
            elif isinstance(child, ast.For):
                for elt in ast.walk(child.target):
                    if isinstance(elt, ast.Name):
                        defined.add(elt.id)
            elif isinstance(child, ast.With):
                for item in child.items:
                    if item.optional_vars and isinstance(item.optional_vars, ast.Name):
                        defined.add(item.optional_vars.id)
            elif isinstance(child, (ast.Import, ast.ImportFrom)):
                for alias in child.names:
                    defined.add(alias.asname if alias.asname else alias.name.split('.')[0])
            elif isinstance(child, (ast.ListComp, ast.SetComp, ast.GeneratorExp, ast.DictComp)):
                generators = getattr(child, 'generators', [])
                for gen in generators:
                    for elt in ast.walk(gen.target):
                        if isinstance(elt, ast.Name):
                            defined.add(elt.id)

        # Collect all Name loads in the function body (excluding the function node itself)
        for child in ast.walk(ast.Module(body=node.body, type_ignores=[])):
            if not isinstance(child, ast.Name):
                continue
            if not isinstance(child.ctx, ast.Load):
                continue

            name = child.id

            # Skip builtins, dunders, and names known to be defined
            if name in defined:
                continue
            if name.startswith('__') and name.endswith('__'):
                continue
            if name in dir(__builtins__) if isinstance(__builtins__, dict) else dir(__builtins__):
                continue

            # Only flag if there's a similarly-named defined variable (edit-distance 1 typo or
            # prefix/suffix truncation) — avoids flagging genuine globals/imports
            close_match = None
            for d in defined:
                # Exact prefix match (e.g. `hour` vs `hours_ahead`)
                if d.startswith(name) or name.startswith(d):
                    close_match = d
                    break
                # Simple one-character difference (insertion/deletion/substitution)
                if abs(len(d) - len(name)) <= 2 and (
                    name in d or d in name or
                    sum(a != b for a, b in zip(name, d)) == 1
                ):
                    close_match = d
                    break

            if close_match is None:
                continue

            issues.append({
                "line": child.col_offset if not hasattr(child, 'lineno') else child.lineno,
                "description": (
                    f"Possibly undefined name `{name}` used in `{node.name}()`; "
                    f"did you mean `{close_match}`? (NameError at runtime)"
                ),
                "severity": "high",
            })

    return issues

_AUTO_PATTERNS.append(_auto_check_003)


# Added 2026-04-17 — caught after fixing: HTTPError in tools/fetch_octo_slots.py:130
def _auto_check_004(source: str, tree: ast.AST, filepath: pathlib.Path) -> list[dict]:
    """Detect OCTO unit dicts using 'id' instead of the spec-required 'unitId' key.

    The OCTO availability API requires units to carry 'unitId', not 'id'.
    Passing 'id' silently produces a 400 HTTPError with no clear message.
    """
    results = []
    if "octo" not in filepath.stem.lower() and "octo" not in source.lower():
        return results

    for node in ast.walk(tree):
        # Look for dict literals like {"id": ..., "quantity": ...}
        if isinstance(node, ast.Dict):
            key_strings = []
            for k in node.keys:
                if isinstance(k, ast.Constant) and isinstance(k.value, str):
                    key_strings.append(k.value)
            if "id" in key_strings and "quantity" in key_strings and "unitId" not in key_strings:
                results.append({
                    "line": node.lineno,
                    "description": (
                        "Unit dict uses 'id' instead of OCTO-required 'unitId'. "
                        "The availability/booking API will return HTTP 400."
                    ),
                    "severity": "high",
                })
        # Look for dict() calls like dict(id=..., quantity=...)
        if isinstance(node, ast.Call):
            if isinstance(node.func, ast.Name) and node.func.id == "dict":
                kw_names = [kw.arg for kw in node.keywords if kw.arg is not None]
                if "id" in kw_names and "quantity" in kw_names and "unitId" not in kw_names:
                    results.append({
                        "line": node.lineno,
                        "description": (
                            "Unit dict uses 'id' instead of OCTO-required 'unitId'. "
                            "The availability/booking API will return HTTP 400."
                        ),
                        "severity": "high",
                    })
    return results

_AUTO_PATTERNS.append(_auto_check_004)
