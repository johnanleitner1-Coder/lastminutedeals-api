def _auto_check_001(source: str, tree: ast.AST, filepath: pathlib.Path) -> list[dict]:
    """
    A11: Payment method used without validating required credential field.
    Detects if-blocks that check a payment_method string ('wallet', 'stripe_pi', etc.)
    and perform a charge/debit action, but have no guard that the corresponding
    credential field (wallet_id, payment_intent_id, etc.) is truthy before the block.
    """
    import ast as _ast

    PAYMENT_METHOD_CREDENTIALS = {
        "wallet": ["wallet_id"],
        "stripe_pi": ["payment_intent_id"],
        "stripe": ["payment_intent_id", "stripe_token"],
        "paypal": ["paypal_token", "paypal_order_id"],
    }

    CHARGE_INDICATORS = {
        "charge", "debit", "capture", "pay", "payment", "transact",
        "wallet_charge", "stripe_charge", "process_payment",
    }

    findings = []

    class PaymentMethodVisitor(_ast.NodeVisitor):
        def __init__(self):
            self.issues = []

        def _node_calls_charge(self, node: _ast.AST) -> bool:
            for child in _ast.walk(node):
                if isinstance(child, _ast.Call):
                    func = child.func
                    name = ""
                    if isinstance(func, _ast.Attribute):
                        name = func.attr
                    elif isinstance(func, _ast.Name):
                        name = func.id
                    if any(ind in name.lower() for ind in CHARGE_INDICATORS):
                        return True
            return False

        def _extract_payment_method_literal(self, test: _ast.AST) -> str | None:
            """Return the payment method string from a comparison like x == 'wallet'."""
            if isinstance(test, _ast.Compare):
                comparators = test.comparators
                if len(comparators) == 1 and isinstance(comparators[0], _ast.Constant):
                    val = comparators[0].value
                    if isinstance(val, str) and val in PAYMENT_METHOD_CREDENTIALS:
                        return val
                if isinstance(test.left, _ast.Constant):
                    val = test.left.value
                    if isinstance(val, str) and val in PAYMENT_METHOD_CREDENTIALS:
                        return val
            return None

        def _body_guards_credential(self, body: list, cred_names: list[str]) -> bool:
            """
            Return True if the body contains an early-exit guard that references
            any of the required credential field names (e.g. `if not req.wallet_id: ...`).
            """
            for stmt in body:
                for node in _ast.walk(stmt):
                    if isinstance(node, (_ast.If,)):
                        test = node.test
                        test_src = _ast.unparse(test) if hasattr(_ast, "unparse") else ""
                        for cred in cred_names:
                            if cred in test_src:
                                # Check that the branch raises, returns, or continues
                                for sub in node.body + node.orelse:
                                    if isinstance(sub, (_ast.Raise, _ast.Return, _ast.Continue)):
                                        return True
                    if isinstance(node, (_ast.Name, _ast.Attribute)):
                        attr = node.attr if isinstance(node, _ast.Attribute) else node.id
                        if any(cred == attr for cred in cred_names):
                            # credential is at least referenced — check if it's in a guard
                            pass
            return False

        def _preceding_stmts_guard_credential(
            self, stmts: list, if_index: int, cred_names: list[str]
        ) -> bool:
            """
            Check statements BEFORE the payment if-block (in the same scope) for
            credential validation guards.
            """
            for stmt in stmts[:if_index]:
                stmt_src = _ast.unparse(stmt) if hasattr(_ast, "unparse") else ""
                for cred in cred_names:
                    if cred in stmt_src:
                        if isinstance(stmt, _ast.If):
                            for sub in stmt.body + stmt.orelse:
                                if isinstance(sub, (_ast.Raise, _ast.Return, _ast.Continue)):
                                    return True
            return False

        def _check_body(self, stmts: list):
            for idx, node in enumerate(stmts):
                if not isinstance(node, _ast.If):
                    continue

                method = self._extract_payment_method_literal(node.test)
                if method is None:
                    continue

                cred_names = PAYMENT_METHOD_CREDENTIALS[method]

                if not self._node_calls_charge(node):
                    continue

                # Check if the if-block itself guards the credential
                if self._body_guards_credential(node.body, cred_names):
                    continue

                # Check if a preceding sibling statement guards it
                if self._preceding_stmts_guard_credential(stmts, idx, cred_names):
                    continue

                # No guard found — flag it
                self.issues.append({
                    "line": node.lineno,
                    "description": (
                        f"payment_method='{method}' branch performs a charge action "
                        f"but does not validate that {cred_names} is present/non-empty "
                        f"before proceeding; a missing credential will silently skip "
                        f"the charge while leaving payment_ok=True."
                    ),
                    "severity": "critical",
                })

        def visit_FunctionDef(self, node: _ast.FunctionDef):
            self._check_body(node.body)
            self.generic_visit(node)

        visit_AsyncFunctionDef = visit_FunctionDef

        def visit_For(self, node: _ast.For):
            self._check_body(node.body)
            self.generic_visit(node)

        def visit_While(self, node: _ast.While):
            self._check_body(node.body)
            self.generic_visit(node)

    visitor = PaymentMethodVisitor()
    visitor.visit(tree)
    findings.extend(visitor.issues)
    return findings

