# Added 2026-04-17 — caught after fixing: security in tools/manage_wallets.py:159
def _auto_check_002(source: str, tree: ast.AST, filepath: pathlib.Path) -> list[dict]:
    """
    A11: Monetary amount parameter not validated positive before financial mutation.

    Detects functions that accept a monetary amount parameter (e.g. amount_cents,
    price_cents) and mutate a balance/wallet field without first guarding that the
    amount is > 0.  A missing guard allows zero-amount no-ops and negative-amount
    free credits (balance_cents = current - (-500) = current + 500).
    """
    import re

    MONEY_PARAM_RE = re.compile(
        r"^(amount|amount_cents|amount_usd|price|price_cents|cost|cost_cents|"
        r"total|total_cents|charge|charge_cents|debit_cents|credit_cents)$",
        re.IGNORECASE,
    )

    FINANCIAL_NAME_RE = re.compile(
        r"(debit|credit|charge|transfer|withdraw|deposit|spend|pay|fund)",
        re.IGNORECASE,
    )

    BALANCE_KEY_RE = re.compile(r"balance|wallet|funds", re.IGNORECASE)

    def _mutates_balance(func_node: ast.FunctionDef) -> bool:
        for node in ast.walk(func_node):
            # wlt["balance_cents"] = ...  or  wallets["balance"] = ...
            if isinstance(node, ast.Assign):
                for tgt in node.targets:
                    if (
                        isinstance(tgt, ast.Subscript)
                        and isinstance(tgt.slice, ast.Constant)
                        and isinstance(tgt.slice.value, str)
                        and BALANCE_KEY_RE.search(tgt.slice.value)
                    ):
                        return True
            # balance_cents -= amount  or  balance_cents += amount
            if isinstance(node, ast.AugAssign):
                tgt = node.target
                name = (
                    tgt.id
                    if isinstance(tgt, ast.Name)
                    else (tgt.attr if isinstance(tgt, ast.Attribute) else "")
                )
                if BALANCE_KEY_RE.search(name):
                    return True
        return False

    def _has_positive_guard(func_node: ast.FunctionDef, param: str) -> bool:
        """
        Return True if one of the first 8 statements is an if/assert that
        raises or returns when param <= 0 (or param < 1, or not param > 0).
        """
        for stmt in func_node.body[:8]:
            # assert param > 0  or  assert param >= 1
            if isinstance(stmt, ast.Assert):
                test = stmt.test
                if isinstance(test, ast.Compare):
                    left = test.left
                    if isinstance(left, ast.Name) and left.id == param:
                        if any(isinstance(op, (ast.Gt, ast.GtE)) for op in test.ops):
                            return True

            if not isinstance(stmt, ast.If):
                continue

            test = stmt.test

            # if param <= 0 / if param < 1
            if isinstance(test, ast.Compare):
                left = test.left
                if isinstance(left, ast.Name) and left.id == param:
                    for op, cmp in zip(test.ops, test.comparators):
                        if isinstance(op, (ast.LtE, ast.Lt)) and isinstance(cmp, ast.Constant):
                            if cmp.value in (0, 1):
                                if any(isinstance(s, (ast.Raise, ast.Return)) for s in stmt.body):
                                    return True

            # if not (param > 0)
            if (
                isinstance(test, ast.UnaryOp)
                and isinstance(test.op, ast.Not)
                and isinstance(test.operand, ast.Compare)
            ):
                inner = test.operand
                if isinstance(inner.left, ast.Name) and inner.left.id == param:
                    if any(isinstance(op, (ast.Gt, ast.GtE)) for op in inner.ops):
                        if any(isinstance(s, (ast.Raise, ast.Return)) for s in stmt.body):
                            return True

        return False

    issues = []

    for node in ast.walk(tree):
        if not isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef)):
            continue

        if not FINANCIAL_NAME_RE.search(node.name):
            continue

        all_args = node.args.posonlyargs + node.args.args + node.args.kwonlyargs

        for arg in all_args:
            if not MONEY_PARAM_RE.match(arg.arg):
                continue

            if _mutates_balance(node) and not _has_positive_guard(node, arg.arg):
                issues.append({
                    "line": node.lineno,
                    "description": (
                        f"Function '{node.name}' accepts monetary parameter '{arg.arg}' "
                        f"but does not validate it is > 0 before mutating wallet/balance state. "
                        f"A zero amount records a silent no-op; a negative amount inverts the "
                        f"arithmetic and credits the wallet for free."
                    ),
                    "severity": "security",
                })
                break  # one finding per function

    return issues

_AUTO_PATTERNS.append(_auto_check_002)


